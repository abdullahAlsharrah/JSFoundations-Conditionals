let FirstNumber = 7;
let SecondNumber = 4;
let operation = "-";

// Write if-statements here


if (operation === "+") {
    console.log(`${FirstNumber} + ${SecondNumber} = ${FirstNumber + SecondNumber}`);
} else if (operation === "*") {
    console.log(`${FirstNumber} * ${SecondNumber} = ${FirstNumber * SecondNumber}`);
} else if (operation === "/"){
    console.log(`${FirstNumber} / ${SecondNumber} = ${FirstNumber / SecondNumber}`);
} else if (operation === "-") {
    console.log(`${FirstNumber} - ${SecondNumber} = ${FirstNumber - SecondNumber}`);
}